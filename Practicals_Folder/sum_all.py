#!/usr/bin/python3

#This program sums all  the numbers from 0 to 100.
print("This program sums all numbers from 0 to 100.")
sum_of_all = 0
for num in range(0, 101, 1):
    sum_of_all = sum_of_all + num
print("The sum of all numbers from 0 to 100 is: ", sum_of_all)
